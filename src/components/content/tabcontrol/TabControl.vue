<template>
<div class="tab-control">
    <div class="tab-control-item"
        v-for="(item, index) in titles" :key="index"
        :class="{active: index === currentIndex}"
        @click="itemClick(index)"
    >
    <span>{{item}}</span>
    </div>
</div>
</template>
<script>
export default {
    props:{
        titles:{
            type:Array,
            default(){
                return []
            }
        }
    },
    data(){
        return{
            currentIndex:0
        }
    },
    methods:{
        itemClick(index){
            this.currentIndex = index,
            // console.log(index)
            this.$emit('tabClick', index)
        }
    }
}
</script>
